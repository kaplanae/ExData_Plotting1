fileUrl <- 'https://archive.ics.uci.edu/ml/machine-learning-databases/00235/household_power_consumption.zip'
download.file(fileUrl, destfile ="./HouseholdPower")
unzip("./HouseholdPower")
HHPower <-read.table(unzip("./HouseholdPower"), header = TRUE, na.strings ="?", sep = ";")
head(HHPower)
str(HHPower)
HHPower <- subset(HHPower, Date == "1/2/2007" | Date == "2/2/2007")
HHPower[, c(3:9)] <- sapply(HHPower[, c(3:9)], as.character)
HHPower[, c(3:9)] <- sapply(HHPower[, c(3:9)], as.numeric)
HHPower$Date <-as.Date(HHPower$Date, format = "%m/%d/%Y")
HHPower$Time <- strptime(HHPower$Time, format ="%X")
png('rplot1.png', width= 480, height = 480)
hist(HHPower$Global_active_power, col="red", xlim = c(0,6), ylim = c(0,1200), xlab = "Global Active Power (kilowatts)", main = "Global Active Power")
dev.off()